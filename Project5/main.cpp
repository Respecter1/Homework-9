int main() {
    int* ptra = new int; // Виділення пам'яті для цілочисельного значення
    *ptra = 42; // Присвоєння значення
    delete ptra; // Звільнення пам'яті
    ptra = nullptr;//ми занулили вказівник після звільнення пам'яті, тим самим уникнувши блукаючого вказівника.


    return 0;
}
